// Definition for a binary tree node.
// #[derive(Debug, PartialEq, Eq)]
// pub struct TreeNode {
//   pub val: i32,
//   pub left: Option<Rc<RefCell<TreeNode>>>,
//   pub right: Option<Rc<RefCell<TreeNode>>>,
// }
//
// impl TreeNode {
//   #[inline]
//   pub fn new(val: i32) -> Self {
//     TreeNode {
//       val,
//       left: None,
//       right: None
//     }
//   }
// }
use std::cell::RefCell;
use std::rc::Rc;
impl Solution {
    pub fn is_balanced(root: Option<Rc<RefCell<TreeNode>>>) -> bool {
        Self::balanced_dfs(&root).is_ok()
    }
    fn balanced_dfs(root: &Option<Rc<RefCell<TreeNode>>>) -> Result<i32, ()> {
        if let Some(ro) = root {
            let lh = Self::balanced_dfs(&ro.borrow().left)?; // ? => return Error if not match
            let rh = Self::balanced_dfs(&ro.borrow().right)?;
            if (lh - rh).abs() > 1 {
                Err(())
            } else {
                Ok(1 + std::cmp::max(lh, rh))
            }
        } else {
            Ok(0)
        }
    }
}
